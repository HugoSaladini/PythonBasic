valor = input('Digite algo: ')
print('O tipo primitivo deste valor é: ', type(valor))
print('Só tem espaços? ', valor.isspace())
print('É um Número? ', valor.isnumeric())
print('É Alphabetico? ', valor.isalpha())
print('É Alphanumérico? ', valor.isalnum())
print('Esta em Maiusculas? ', valor.isupper())
print('Esta em Minusculas?', valor.islower())
print('Esta Captalizada? ', valor.istitle())




